%% Exporting EBSD Data
%
%%
% MTEX allows you to export EBSD data as <EBSD.export_h5.html h5>,
% <EBSD.export_ctf.html ctf>, <EBSD.export_ang.html ang> or
% <EBSD.export_cpr.html cpr/crc> files. The syntax is
%
%   ebsd.export('myFile.ctf')
%
% The file format is detected automatically from the the extension of the
% specified file name.
%
% Note that information may be lost during export into a format different
% then the import format as not all file formats support all properties.
%


