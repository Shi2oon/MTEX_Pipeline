%% The MTEX Documentation
%
% Documenting a project like MTEX is a challenging and ongoing task for the
% entire community. Therefore we are extremely happy about any spelling
% fixes, examples, theoretical explainations, special use cases, etc. As a
% bonus everybody who contributed to MTEX will automatically appear at the
% <https://github.com/mtex-toolbox/mtex/graphs/contributors contributors
% page>.
%
%% Making Changes Online
%
% The easiest way to suggest changes to the MTEX documentation is online
% visa Github. This is done as follows:
%
% # sign into <https://github.com GitHub>
% # open the help page you want to change, e.g., this one
% # click
% <https://github.com/mtex-toolbox/mtex/blob/develop/doc/GeneralConcepts/Contribute2Doc.m
% edit page> - this leads you to the file on <https://github.com GitHub>
% # click the little pencil in the top right corner which allows you to
% edit the script online
% # enter of copy and paste your changes into the site
% # click at the bottom *suggest changes*
% # click create *pull request*
%
%% Making Changes Within Matlab
%
% Since all documentation pages are essentially MTEX scripts they can be
% opened in Matlab by the command
%
%   edit hepPageName
%
% This allows you to run through all the commands and investiage the effect
% of different parameters or different input files or add further analysis
% steps.
%
% If you like the changes you made simply copy and paste the changes into
% the GitHub page as explained above or send it to one of the developers by
% email.
% 
%% Publish command
%
% You may can a visual preview of your altered documentation page by using
% the matlab function
% <https://de.mathworks.com/help/matlab/matlab_prog/publishing-matlab-code.html
% publish> 
%
%   publish filename
%
% which creates an folder, named 'html', in your current directory where
% you can find the generated html page.
%
